/* Switch case statement in JS */

const month = 4

switch (month) {
  case 1:
    console.log("January");        
    break;
  case 2:
    console.log("February");        
    break;
  case 3:
    console.log("March");        
    break;
  case 4:
    console.log("April");        
    break;
  case 5:
    console.log("May");        
    break;

  default:
    console.log("No months are matched.");
    break;
} // April